// Esto es lo que se mostrará en la pantalla
bool oled_task_user(void) {
    led_t led_state = host_keyboard_led_state(); // Nos da el estado del block num y CAPS lock
    if (is_keyboard_master()) {
        switch (get_highest_layer(layer_state)) {
            case _LAYER1:
                oled_write_P(PSTR("Lyr:\n"), false);
                oled_write_P(PSTR("Num\n"), false);
                break;
            case _LAYER2:
                oled_write_P(PSTR("Lyr:\n"), false);
                oled_write_P(PSTR("Sig\n"), false);
                break;
            case _LAYER3:
                oled_write_P(PSTR("Lyr:\n"), false);
                oled_write_P(PSTR("Conf\n"), false);
                break;
            default:
                oled_write_P(PSTR("Lyr:\n"), false);
                oled_write_P(PSTR("Base\n"), false);
                break;
        }
        oled_write_P(led_state.caps_lock ? PSTR("CAP\n") : PSTR("\n"), false);
        render_animation();

        oled_set_cursor(0, 8);
        oled_write_P(PSTR("WPM: "), false);
        oled_write(get_u8_str(get_current_wpm(), '0'), false);
    } else {
        render_logo();
    }

    return false;
}

